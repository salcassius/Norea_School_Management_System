<?php

namespace App\Http\Controllers\API;

use App\Http\Controllers\Controller;
use App\Models\Subject;
use Illuminate\Http\Request;

class SubjectController extends Controller
{
    public function index()
    {
        // бһ‘бһ¶бһүбһҷбһҖбһҳбһ»бһҒбһңбһ·бһҮбҹ’бһҮбһ¶бһ‘бһ¶бҹҶбһ„бһўбһҹбҹӢ бһ–бҹ’бһҡбһҳбһҮбһ¶бһҳбһҪбһҷбһ–бҹҗбһҸбҹҢбһҳбһ¶бһ“бһӮбҹ’бһҡбһј бһ“бһ·бһ„бһҗбҹ’бһ“бһ¶бһҖбҹӢ
        $subjects = Subject::with(['class', 'teacher'])->get();
        
        return response()->json($subjects);
    }

    public function store(Request $request) 
    {
        // бһҖбҹӮбһҹбһҳбҹ’бһҡбһҪбһӣ Validation бһұбҹ’бһҷбһ–бһ·бһ“бһ·бһҸбҹ’бһҷбһ‘бһ¶бҹҶбһ„бһҗбҹ’бһ“бһ¶бһҖбҹӢбһҡбҹҖбһ“ бһ“бһ·бһ„бһӮбҹ’бһҡбһјбһ”бһ„бҹ’бһҡбҹҖбһ“
        $data = $request->validate([
            'name'       => 'required|string|max:255',
            'class_id'   => 'required|exists:classes,id', 
            'teacher_id' => 'nullable|exists:teachers,id', // рҹ’Ў бһ”бһҫбһҖбһҠбҹҶбһҺбһҫбһҡбһҖбһ¶бһҡ бһ“бһ·бһ„бһўбһ“бһ»бһүбҹ’бһүбһ¶бһҸбһұбҹ’бһҷбһ‘бҹ…бһҮбһ¶ null бһ”бһ¶бһ“
        ], [
            'name.required'       => 'бһҹбһјбһҳбһ”бһүбҹ’бһ…бһјбһӣбһҲбҹ’бһҳбҹ„бҹҮбһҳбһ»бһҒбһңбһ·бһҮбҹ’бһҮбһ¶',
            'class_id.required'   => 'бһҹбһјбһҳбһҮбҹ’бһҡбһҫбһҹбһҡбһҫбһҹбһҗбҹ’бһ“бһ¶бһҖбҹӢбһҡбҹҖбһ“',
            'class_id.exists'     => 'бһҗбҹ’бһ“бһ¶бһҖбҹӢбһҡбҹҖбһ“бһҠбҹӮбһӣбһ”бһ¶бһ“бһҮбҹ’бһҡбһҫбһҹбһҡбһҫбһҹбһҳбһ·бһ“бһҸбҹ’бһҡбһ№бһҳбһҸбҹ’бһҡбһјбһңбһЎбһҫбһҷ',
            'teacher_id.exists'   => 'бһӮбҹ’бһҡбһјбһ”бһ„бҹ’бһҡбҹҖбһ“бһҠбҹӮбһӣбһ”бһ¶бһ“бһҮбҹ’бһҡбһҫбһҹбһҡбһҫбһҹбһҳбһ·бһ“бһҳбһ¶бһ“бһҖбҹ’бһ“бһ»бһ„бһ”бҹ’бһҡбһ–бҹҗбһ“бҹ’бһ’бһЎбһҫбһҷ',
        ]);

        // бһ”бһ„бҹ’бһҖбһҫбһҸ Record бһҗбҹ’бһҳбһёбһҖбҹ’бһ“бһ»бһ„ Database
        $subject = Subject::create([
            'name'       => $data['name'],
            'class_id'   => $data['class_id'],
            'teacher_id' => $data['teacher_id'] ?? null, // рҹ’Ў бһҡбһҖбҹ’бһҹбһ¶бһ‘бһ»бһҖ ID бһӮбҹ’бһҡбһјбһ”бһ„бҹ’бһҡбҹҖбһ“бһ…бһјбһӣбһҖбҹ’бһ“бһ»бһ„бһҸбһ¶бһҡбһ¶бһ„
        ]);

        // бһ”бһүбҹ’бһҮбһјбһ“бһ‘бһ·бһ“бҹ’бһ“бһ“бҹҗбһҷбһҠбҹӮбһӣбһ”бһ„бҹ’бһҖбһҫбһҸбһҡбһҪбһ…бһҸбҹ’бһҡбһЎбһ”бҹӢбһ‘бҹ…бһңбһ·бһү бһҮбһ¶бһҳбһҪбһҷ Status 201 (Created)
        return response()->json([
            'message' => 'бһ”бһ„бҹ’бһҖбһҫбһҸбһҳбһ»бһҒбһңбһ·бһҮбҹ’бһҮбһ¶бһ”бһ¶бһ“бһҮбҹ„бһӮбһҮбҹҗбһҷ',
            'data'    => $subject->load(['teacher', 'class']) 
        ], 201);
    }

    // рҹ’Ў бһ”бһ“бҹ’бһҗбҹӮбһҳбһҳбһ»бһҒбһ„бһ¶бһҡбһҖбҹӮбһҹбһҳбҹ’бһҡбһҪбһӣ (Update) бһҹбһҳбҹ’бһҡбһ¶бһ”бҹӢбһ•бҹ’бһ‘бһ¶бҹҶбһ„ EditeSubject.vue
    public function update(Request $request, $id)
    {
        $subject = Subject::find($id);

        if (!$subject) {
            return response()->json(['message' => 'бһҡбһҖбһҳбһ·бһ“бһғбһҫбһүбһҳбһ»бһҒбһңбһ·бһҮбҹ’бһҮбһ¶бһҠбҹӮбһӣбһҸбҹ’бһҡбһјбһңбһҖбҹӮбһ”бҹ’бһҡбҹӮбһЎбһҫбһҷ'], 404);
        }

        // Validation бһҠбһјбһ…бһ‘бҹ…бһ“бһ№бһ„бһҖбһ¶бһҡ Store бһҠбҹӮбһҡ
        $data = $request->validate([
            'name'       => 'required|string|max:255',
            'class_id'   => 'required|exists:classes,id',
            'teacher_id' => 'nullable|exists:teachers,id', 
        ], [
            'name.required'     => 'бһҹбһјбһҳбһ”бһүбҹ’бһ…бһјбһӣбһҲбҹ’бһҳбҹ„бҹҮбһҳбһ»бһҒбһңбһ·бһҮбҹ’бһҮбһ¶',
            'class_id.required' => 'бһҹбһјбһҳбһҮбҹ’бһҡбһҫбһҹбһҡбһҫбһҹбһҗбҹ’бһ“бһ¶бһҖбҹӢбһҡбҹҖбһ“',
            'class_id.exists'   => 'бһҗбҹ’бһ“бһ¶бһҖбҹӢбһҡбҹҖбһ“бһҠбҹӮбһӣбһ”бһ¶бһ“бһҮбҹ’бһҡбһҫбһҹбһҡбһҫбһҹбһҳбһ·бһ“бһҸбҹ’бһҡбһ№бһҳбһҸбҹ’бһҡбһјбһңбһЎбһҫбһҷ',
            'teacher_id.exists' => 'бһӮбҹ’бһҡбһјбһ”бһ„бҹ’бһҡбҹҖбһ“бһҠбҹӮбһӣбһ”бһ¶бһ“бһҮбҹ’бһҡбһҫбһҹбһҡбһҫбһҹбһҳбһ·бһ“бһҳбһ¶бһ“бһҖбҹ’бһ“бһ»бһ„бһ”бҹ’бһҡбһ–бҹҗбһ“бҹ’бһ’бһЎбһҫбһҷ',
        ]);

        // бһ’бҹ’бһңбһҫбһ”бһ…бҹ’бһ…бһ»бһ”бҹ’бһ”бһ“бҹ’бһ“бһ—бһ¶бһ–бһ‘бһ·бһ“бҹ’бһ“бһ“бҹҗбһҷ
        $subject->update([
            'name'       => $data['name'],
            'class_id'   => $data['class_id'],
            'teacher_id' => $data['teacher_id'] ?? null, // бһ”бһҫбһҳбһ·бһ“бһ”бһ¶бһ“бһҮбҹ’бһҡбһҫбһҹбһҡбһҫбһҹбһӮбҹ’бһҡбһј бһңбһ¶бһ“бһ№бһ„бһ‘бҹ…бһҮбһ¶ null
        ]);

        return response()->json([
            'message' => 'бһҖбҹӮбһҹбһҳбҹ’бһҡбһҪбһӣбһҳбһ»бһҒбһңбһ·бһҮбҹ’бһҮбһ¶бһ”бһ¶бһ“бһҮбҹ„бһӮбһҮбҹҗбһҷ',
            'data'    => $subject->load(['teacher', 'class']) // Load information бһЎбһҫбһ„бһңбһ·бһүбһҠбһҫбһҳбҹ’бһ”бһёбһ”бһ„бҹ’бһ бһ¶бһүбһӣбһҫ Vue Table бһ—бҹ’бһӣбһ¶бһҳбҹ—
        ], 200);
    }

    // бһҳбһ»бһҒбһ„бһ¶бһҡбһӣбһ»бһ” (Delete)
    public function destroy($id)
    {
        $subject = Subject::find($id);
        if ($subject) {
            $subject->delete();
            return response()->json(['message' => 'бһӣбһ»бһ”бһ”бһ¶бһ“бһҮбҹ„бһӮбһҮбҹҗбһҷ']);
        }
        return response()->json(['message' => 'бһҡбһҖбһҳбһ·бһ“бһғбһҫбһүбһҳбһ»бһҒбһңбһ·бһҮбҹ’бһҮбһ¶'], 404);
    }
}